    马: "ma",
    杨: "yang",
    朱: "zhu",
    胡: "hu",
    高: "gao",
    何: "he",
    一: "yi",
    二: "er",
    三: "san",
    四: "si",
    五: "wu",
    六: "liu",
    七: "qi",
    八: "ba",
    九: "jiu",
    十: "shi",
    明: "ming",
    华: "hua",
    强: "qiang",
    勇: "yong",
    伟: "wei",
    杰: "jie",
    峰: "feng",
    磊: "lei",
    涛: "tao",
    超: "chao",
