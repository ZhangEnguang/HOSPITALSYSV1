    const deadlineDate = new Date(submittedDate)
    deadlineDate.setDate(submittedDate.getDate() + Math.floor(Math.random() * 10) + 5)

    // 完成日期在提交日期和截止日期之间
    const completedDate = new Date(submittedDate)
    const daysToDeadline = Math.floor((deadlineDate.getTime() - submittedDate.getTime()) / (1000 * 60 * 60 * 24))
